                <h2>Культура и традиции</h2>
                <p>Полинезийская культура чрезвычайно богата, выразительна и самобытна. Ее каркасом являются верования и философия островитян, основанные на глубоком понимании и уважении природы, единстве человека с окружающим миром. Знания полинезийцев о происхождении земли и человека, устройстве мира и законах природы передавались из уст в уста при помощи мифологических сказаний и легенд.</p>
                <p>Местное население обладает скромным, радушным, приветливым и трудолюбивым нравом. Островное расположение и климатические условия способствуют размеренному, неторопливому образу жизни. Главной ценностью полинезийцев является семья и дети, также с большим уважением относятся к старшим.</p>
                <p>Основными ремесленными направлениями в стране были и остаются строительство лодок, создание украшений из ракушек и жемчуга, резьба по дереву и камню.</p>
                <p>Неотъемлемой частью местной культуры являются песни и танцы. Местные жители танцуют буквально по любому поводу — ради развлечения, для того, чтобы бросить вызов сопернику, для празднования победы. «Отеа» исполняют исключительно мужчины, «упа-упа» — парный танец. В качестве аккомпанемента выступают струнные и духовые музыкальные инструменты, например, носовая флейта «виво».</p>
                <p>Совершенно уникальным проявлением полинезийской культуры является искусство татуировки. Пожалуй, именно здесь татуировка достигла наибольшего разнообразия форм и стилей, а также качества ее исполнения. Нательные рисунки, помимо эстетического значения, играли роль социальных знаков — по ним можно было определить статус человека и принадлежность к тому или иному сословию и роду. Удивительно, что при археологических раскопках учеными были обнаружены специальные инструменты для нанесения татуировки на тело, возраст которых исчисляется тысячелетиями, то есть вид данного искусства стоял у истоков полинезийской цивилизации.</p>

