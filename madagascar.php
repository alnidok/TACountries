                <h2>Культура и традиции</h2>
                <p>Благодаря своему географическому положению, а именно удаленности от континента, Мадагаскар сумел сохранить самобытность и уникальность местной культуры. Несмотря на доминирующее христианство, здесь сохранился культ предков, различные запреты, табу и обряды.</p>

                <h5>Обряды</h5>
                <p>Малагасийцы очень суеверны, они придерживаются огромного множества различных примет. Здесь даже существует целая система поведенческих табу — «фади». В каждом поселении свои собственные запреты — одни не едят свинину, другие — коз, кто-то не работает по определенным дням или не носит одежду белого цвета. Считается, что, придерживаясь данной системы, человек защищает себя от злых духов. В случае нарушения какого-либо запрета необходимо принести жертву богам, к примеру, вылить на землю бутылку рома.</p>
                <p>В центральной части острова сохранился обряд фамадихана, когда каждые 5 лет прах умершего родственника достают из могилы, оборачивают в новый саван и производят перезахоронение. Обряд основан на убеждении, что души умерших воссоединяются с душами предков только после разложения тела.</p>
                
                <h5>Ремесла</h5>
                <p>Традиционные ремесла малагасийцев — резьба по дереву (фигурки, статуэтки, маски, столпы), ткачество, вышивка, плетение корзин, шляп и циновок, изготовление деревянной посуды и игрушек, производство ювелирных украшений и бижутерии.</p>

                <h5>Кухня</h5>
                <p>Основу местной кухни составляет рис, к которому подаются овощи, мясо и рыба. Малагасийцы любят добавлять в блюда острый стручковый перец и различные пряности. Непременно стоит попробовать «равитуту» (тушеная говядина с пюре из листьев кокоса и маниоки) и «румазаву» (пряное рагу с томатами). Поклонников европейской кухни покорит местная гусиная печенка, а также блюда с морепродуктами (крабы, устрицы, лангусты, морские ежи). На Мадагаскаре развито производство вин и других алкогольных напитков, что неудивительно, учитывая, французское колониальное прошлое.</p>

