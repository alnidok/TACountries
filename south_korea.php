                <h2>Культура и традиции</h2>
                <p>История Южной Кореи изобилует весьма драматическими моментами —  вплоть до 60-х годов XX века страна являлась одной из беднейших в мире, но уже через несколько лет в Республике Корее начался период активного экономического роста, повлекшего за собой ориентацию образа жизни на западные эталоны. Тем не менее удалось сохранить многие традиции, которые по сей день являются яркими чертами самобытной азиатской страны.</p>
                <p>Практически всё общество в стране живет согласно конфуцианскому учению, независимо от вероисповедания. Конфуцианство здесь является больше философией, жизненной мудростью, нежели религией. Одной из характерных национальных черт является коллективизм. Индивидуализм же критикуется в учениях конфуцианства, даосизма и буддизма, считая, что он заслоняет от человека истинную природу реальности. Здесь очень важную роль играют личные связи — семейные, дружеские, служебные.</p>
                <p>В жизни каждого корейца самое главное место занимает семья, где наблюдается устойчивая иерархия, во главе которой находится мужчина. Женщина исполняет роль хранительницы семейных обычаев и традиций, роль в воспитании ребенка в семье отведена абсолютно всем родственникам. Иерархичность характерна не только в семье, но и в обществе в целом. У корейцев при первой встрече принято спрашивать о семье, возрасте, учебном заведении, месте работы, должности и т.д. Эта информация позволяет установить социальное положение собеседника и понять в каком ключе себя следует с ним вести. К примеру, для местных жителей очевидно, что, женатый человек находится на иерархической лестнице выше холостяка, а выпускник столичного университета — гораздо выше выпускника из провинции.</p>
                <p>Ярким проявлением местной культуры являются уникальная корейская живопись на тонкой бумаге или шелке, театральное искусство (опера, драма, балет), живые и динамичные народные танцы, лирическая и душевная музыка.</p>
                <p>Национальная корейская кухня достойна отдельного внимания. Ее основой является рис, как и в большинстве азиатских стран, но все же корейская кухня обладает своими исключительными чертами. Так, например, в местные блюда добавляется много красного перца для лучшей сохранности продуктов в жарком климате. Здесь понятия «острый» и «вкусный» являются практически синонимами. Самые популярные блюда из риса — плов «пибимпап», каша «пап», отбивные хлебцы «тток», его также добавляют в супы, сэндвичи и омлет. Очень часто рис является основным блюдом, к которому подается множество различных закусок — овощных, мясных или из морепродуктов. Местные десерты — это засахаренные фрукты и фруктовые салаты. Самое любимое лакомство корейцев — «ходуквачжа» — круглое печенье с разнообразной начинкой. Из неалкогольных напитков употребляют травяные чаи, а в основе алкогольных напитков, конечно же, все тот же рис — из него изготавливают традиционное вино, водку и пиво.</p>

