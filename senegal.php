                <h2>Культура и традиции</h2>
                <p>Культура Сенегала чрезвычайно богата, выразительна и самобытна. Она представляет собой удивительную традиционную Западно-Африканскую культуру, в большей степени народа волоф, с вкраплениями французских культурных традиций. Ее каркасом являлись и остаются по сей день религиозные практики местных жителей.</p>

                <h5>Менталитет</h5>
                <p>Жители Сенегала в основной своей массе очень приветливы и дружелюбны, обладают чувством собственного достоинства. Кстати, сенегалки считаются самыми красивыми женщинами Африканского континента.</p>
                <p>У местных жителей существует своя система знаков внимания к собеседникам с различным социальным статусом. Вообще, в обществе до сих пор прослеживается четкая дифференциация по кастовым и родовым признакам. Так, выделяются гееры (свободнорожденные), гарми (правящие), домибур (знать), свободные общинники и ремесленники — кузнецы, ювелиры, ткачи и т.д. Все серьезные решения, как правило, принимаются имамом или пожилым богословом.</p>
                
                <h5>Ремесла</h5>
                <p>Традиционные художественные ремесла сенегальцев — резьба по дереву (фигурки, статуэтки, маски), выделка кожи и производство кожаных аксессуаров, ткачество, вышивка, роспись стекла, плетение корзин, шляп и циновок, изготовление деревянной посуды и игрушек. Особым образом выделяется ювелирное мастерство — изысканные золотые и серебряные изделия, изготовленные народом волоф, пользуются спросом даже далеко за пределами страны.</p>
                <p>В Дакаре (район Сумбедьюн) есть своеобразная деревня ремесленников, где туристы могут увидеть процесс работы сенегальских мастеров и приобрести их замечательные работы.</p>

                <h5>Кухня</h5>
                <p>Основу местной кухни составляет рис, к которому подаются мясо, овощи, рыба и, конечно же, морепродукты. Обязательно стоит отведать сенегальские коронные блюда: вкуснейшую «дьяггу» — рыбные тефтели с красным или белым соусом «тибу-дьенн», «румазаву» — томатное пряное рагу и «равитуту» — тушеную говядину с кокосом и маниокой.</p>
                <p>Из напитков особенно популярны мятный чай, кофе с гвоздикой и перцем, а также довольно экзотический напиток — сок баобаба, обладающий приятным вкусом и чрезвычайно полезными свойствами. Кстати, сами сенегальцы называют баобаб деревом-аптекой или деревом жизни.</p>

