                <h2>Климат</h2>
                
                <p>На побережье Гренландии господствует морской субарктический климат. Он является наиболее мягким на юго-западе острова, где сосредоточено большинство населенных пунктов Гренландии. Средние температуры июля здесь составляют от +3 °C до +10 °C, а января — от −7 °С до −11 °С.</p>
                <p>В центральной части острова отрицательные температуры сохраняются даже летом — в среднем −12 °C, а зимой около −47 °C.</p>
                <p>На всей территории часто бушуют циклоны, приносящие с собой сильный ветер «питерак» (до 70 м/сек), осадки и резкие изменения температуры. Большая часть осадков выпадает зимой и осенью, однако снег может пойти даже летом.</p>
                <p>Лучшее время для посещения Гренландии — в период белых ночей с мая по июль или для поклонников зимних видов спорта — в апреле.</p>
