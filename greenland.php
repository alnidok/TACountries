                <h2>Культура и традиции</h2>

                <h5>Менталитет</h5>
                <p>Несмотря на то, что в современную жизнь Гренландии прочно вошли компьютеры, автомобили, яхты и качественная еда, еще несколько десятилетий назад местные жители вели традиционный образ жизни и существовали, в основном, за счет охоты и рыбалки.</p>
                <p>Жизнь гренландцев, как и сотни лет назад, течет спокойно и размеренно. Местные жители отличаются некоторой сдержанностью эмоций и молчаливостью — они не любят вести праздные беседы и считают, что если говорить не по делу, то слова теряют смысл.</p>
                <p>Инуиты всегда отличались сложной духовной жизнью, основанной на поклонении природе и различным духам. По сей день у коренных жителей прослеживается особенная гармония с землей и трепетное уважение ко всему живому, при этом очень ценятся охотничьи навыки и способность выживать в суровых условиях Гренландии.</p>

                <h5>Искусство</h5>
                <p>Часть инуитов зарабатывает себе на жизнь изготовлением сувениров, которые являются настоящими произведениями искусства, ведь их не штампуют по шаблону, а создают вручную. Самые популярные сувениры — фигурки духов, изготовленные из дерева, камня или даже зубов и костей животных.</p>
                <p>Также из местных камней создают удивительно красивые украшения и бижутерию. Некоторые самородки, например, розовый tugtupit, добывают только в Гренландии.</p>
                <p>Кстати, приобретая украшение, не забудьте попросить у продавца сертификат на него, что позволит вывезти изделие с острова.</p>

                <h5>Кухня</h5>
                <p>Удивительно, но традиционная кухня Гренландии не предполагает никакой термообработки продуктов. Так, китовая кожа с прослойкой жира является любимым деликатесом инуитов. Также довольно популярно блюдо, составляющими которого являются жир нарвала, моржовый мозг и перебродившая трава из желудка оленя.</p>
                <p>Разумеется, не каждый турист решится отведать столь экстремальную гренландскую кухню — к счастью, в местных заведениях есть и более привычные европейские блюда. Обязательно стоит отведать местные морепродукты: печень трески, крабы, креветки, яйца морских птиц и мясо акулы.</p>
                <p>Из напитков популярен черный чай с молоком, в который по желанию добавляется жир, соль и специи. Фирменный кофе в Гренландии — «каффемик» — с тремя видами алкоголя и взбитыми сливками.</p>
