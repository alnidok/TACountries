                <h5>Традиции и менталитет</h5>
                <p>Главные национальные черты местного населения — трудолюбие, доброжелательность, приветливость. Довольно сильно сказывается влияние буддизма. Здесь не принято скандалить или просто ярко выражать свои эмоции на публике. Религиозностью объясняется и низкий уровень преступности в Мьянме. Местные жители честные, простые, всегда готовы помочь туристам, даже если не владеют английским языком.</p>
                <p>Основная часть населения Мьянмы — крестьяне, занимающиеся сельским хозяйством. Проживают они в скромных хижинах, обходясь малым. Тем не менее, каждая семья, вне зависимости от уровня дохода, считает своим долгом принять участие в строительстве пагоды или построить свою.</p>
                <p>Национальная одежда — мужские и женские юбки до самых пят, которые оборачиваются несколько раз вокруг тела. Женщины в качестве украшения обмазывают нос и щеки белой смесью — танакой, в этом есть и рациональное зерно, ведь таким образом лицо защищено от обгорания.</p>

                
                <h5>Искусство</h5>
                <p>Мьянма богата на мастеров и ремесленников. В деревнях у озера Инле сохранилось традиционное производство — ювелирное, кузнечное и гончарное. Мастерские можно посетить всем желающим и приобрести сувениры на память.</p>
                <p>Есть в Мьянме и уникальное место — единственная в мире ткацкая фабрика, где в качестве сырья используют лотосовый стебель. Это очень трудоемкая технология, практически все операции совершаются вручную. За месяц ежедневной работы мастерицы создают всего около 50 метров удивительно тонкой, невесомой, немнущейся ткани. Из нее шилась одежда для королей и зажиточных слоев населения, обычные крестьяне попросту не могли себе ее позволить. Да и сейчас шарф из лотосового шелка в десять раз дороже обычного.</p>
                

                <h5>Кухня</h5>
                <p>Яркая кухня Мьянмы вобрала в себя лучшие гастрономические традиции региона. Как и в кухнях Индии и Китая, здесь главенствует рис, из которого готовят почти все блюда, в том числе и десерты.  Существует даже местная поговорка: «рис – это господин, а люди — его слуги». Из продуктов животного происхождения чаще всего употребляют птицу и морепродукты, а вот мясо (баранину) — крайне редко, по праздникам. Свинина и говядина в стране находятся под запретом буддизма и ислама.</p>
                <p>Обязательно стоит попробовать наиболее характерные блюда местной кухни: «леток сон» — острый салат с рисом и овощами, «хин» — мясо цыпленка или рыба и рис со специями, «моинка» — пряный рыбный суп с лапшой.</p>
