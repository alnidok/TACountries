                <h2>Культура и традиции</h2>
                <p> Японские культура и менталитет очень отличаются от западных благодаря географической изолированности от других государств, а также природным и климатическим особенностям. У японцев собственная уникальная система ценностей, мировоззрения, поведения, реакций и предпочтений. Этим различиям посвящена не одна сотня диссертаций и научных статей. </p>

                <h5>Менталитет</h5>
                <p>Одной из характерных национальных черт является коллективизм. Индивидуализм же критикуется в учениях конфуцианства, даосизма и буддизма, считая, что он заслоняет от человека истинную природу реальности. Здесь очень важную роль играют личные связи — семейные, дружеские, служебные. В жизни каждого японца самое главное место занимает семья — к браку относятся очень серьезно, поэтому в стране редко женятся и выходят замуж раньше 30 лет, а средний возраст для рождения первого ребенка у японок — 34 года. Кстати, ребенку здесь до 5-летнего возраста позволительно абсолютно всё, однако у него появляется масса обязанностей, как только он начинает ходить в школу.</p>
                <p>В Японии с огромным уважением относятся к пожилым людям, можно даже сказать, что здесь культ старости. Интересно, что одним из благожелательных символических подарков является креветка, символизирующая сгорбленную спину старика.</p>
                <p>Японцы — одна из самых трудолюбивых и дисциплинированных наций – здесь не принято уходить на больничный и брать отпуск, часто работают по 18 часов в сутки. Несомненно, что именно эти черты менталитета и делают из японцев одну из наиболее прогрессивных наций.</p>

                <h5>Искусство</h5>
                <p>Многие всемирно известные виды искусства начали свое развитие именно в стране восходящего солнца. К ним относятся: оригами – умение создавать разнообразные фигуры из бумаги, икебана — искусство аранжировки цветов, бонсай — выращивание декоративных карликовых деревьев, каллиграфия — искусство эстетического письма иероглифов, а также японская живопись, роспись тканей, театр кабуки и многое другое.</p>
                <p>Все виды японского искусства несут в себе основные идеи – умиротворение, расслабление и спокойствие.</p>

                <h5>Культура еды</h5>
                <p>Еда для японцев — это гораздо больше, чем просто средство утоления голода. С древних времен в Японии существовал культ еды, и огромную роль играло всё, что с ней связано: посуда, сервировка стола, музыкальное сопровождение и даже одежда слуг, подающих блюда. В настоящее время этот культ сохранился, однако ушла некоторая церемониальность. Деловые, дружеские и романтические встречи принято проводить в ресторанах или кафе.</p>
                <p>В основе большинства блюд — рис и морепродукты. Популярна также лапша из пшеницы (удон) или из гречки (соба). В отличие от других азиатских стран японцы не злоупотребляют специями и пряностями, так как считают их вредными для желудка. Взамен используется множество различных соевых соусов.</p>
                <p>В стране пьют очень много чая, и важен не столько сам факт чаепития, сколько старинный ритуал чайной церемонии, подразумевающей особую технологию приготовления этого напитка.</p>
