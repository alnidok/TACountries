                <p>Туристам выгодно приобретать проездной билет JR Pass, который позволяет без ограничений перемещаться на поездах, паромах и автобусах компании Japan Rail и некоторых частных компаний в течение определенного срока (от одной недели). Стоимость проездного — 30 тыс. JPY на 7 дней, 47 тыс. JPY — на 14.</p>

                <h5>Водный транспорт</h5>
                <p>Ввиду островного расположения морской транспорт в Японии играет очень важную роль.</p>
                <p>Паромы связывают между собой четыре главных острова и меньшие острова. Существует также международное паромное сообщение с Китаем, Россией, КНДР, Тайванем и Южной Кореей.</p>

                <h5>Городской транспорт</h5>
                <p>Транспорт в крупных городах (Токио, Саппоро, Киото, Нагоя, Осака и др.) представлен автобусами, метро и такси. Автобусами иностранным туристам пользоваться несколько проблематично, поскольку практически все указатели и объявление остановок — только на японском языке. В этом плане метро предпочтительнее, где вся информация дублируется на английском. Стоимость поездки варьируется в зависимости от маршрута — от 120 до 1 500 JPY.</p>
                <p>Что касается такси, то его можно заказать по телефону или поймать прямо на улице. Оплата производится по счетчику: посадка стоит от 500 JPY и дополнительно оплачивается каждый километр (от 80 JPY). Необходимо учитывать, что в ночное время стоимость проезда увеличивается на 30%, а раннее бронирование, простой и провоз крупногабаритного багажа оплачиваются дополнительно.</p>
                
