                <h5>Искусство</h5>
                <p>Традиционное искусство Турции хотя и испытало на себе влияние разных исторических событий, все еще хорошо прослеживается в местных ремеслах: ковроткачестве, керамике в стиле чанаккале, производстве анатольевских украшений и ювелирных изделий, плетении кружев айдын, производстве фигурок и скульптур из оникса, изготовлении ятаганов из серебра, медных изделий и многого другого.</p>
